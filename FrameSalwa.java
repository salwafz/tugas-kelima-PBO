    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        if (tfIdSiswa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfNamaSiswa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfTanggalLahir.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfJenisKelamin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            try {
                Class.forName(driver);
                conn = DriverManager.getConnection(koneksi, user, password);
                conn.setAutoCommit(false);

                String sql = "INSERT INTO siswa (id_siswa, nama_siswa, tanggal_lahir, jenis_kelamin, alamat) VALUES (?, ?, ?, ?, ?)";
                pstmt = conn.prepareStatement(sql);

                String Id_Siswa, Nama_Siswa, Tanggal_Lahir, Jenis_Kelamin, Alamat;
                Id_Siswa = tfIdSiswa.getText();
                Nama_Siswa = tfNamaSiswa.getText();
                Tanggal_Lahir = tfTanggalLahir.getText();
                Jenis_Kelamin = tfJenisKelamin.getText();
                Alamat = tfAlamat.getText();

                pstmt.setString(1, Id_Siswa);
                pstmt.setString(2, Nama_Siswa);
                pstmt.setDate(3, java.sql.Date.valueOf(Tanggal_Lahir));
                pstmt.setString(4, Jenis_Kelamin);
                pstmt.setString(5, Alamat);

                pstmt.executeUpdate();
                conn.commit();
                pstmt.close();
                conn.close();

                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + ex.getMessage());
            }

        }

        bersih();
        tampil();

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String Id_Siswa, Nama_Siswa, Tanggal_Lahir, Jenis_Kelamin, Alamat;
        if (tfIdSiswa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfNamaSiswa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfTanggalLahir.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfJenisKelamin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else if (tfAlamat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Isi semua data");
        } else {
            try {
                Class.forName(driver);
                String sql = "UPDATE siswa SET nama_siswa = ?, tanggal_lahir = ?, jenis_kelamin = ?, alamat = ? WHERE id_siswa = ?";
                conn = DriverManager.getConnection(koneksi, user, password);
                pstmt = conn.prepareStatement(sql);

                Id_Siswa = tfIdSiswa.getText();
                Nama_Siswa = tfNamaSiswa.getText();
                Tanggal_Lahir = tfTanggalLahir.getText();
                Jenis_Kelamin = tfJenisKelamin.getText();
                Alamat = tfAlamat.getText();

                pstmt.setString(1, Nama_Siswa);
                pstmt.setDate(2, java.sql.Date.valueOf(Tanggal_Lahir)); // Pastikan formatnya benar
                pstmt.setString(3, Jenis_Kelamin);
                pstmt.setString(4, Alamat);
                pstmt.setString(5, Id_Siswa);

                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
                    pstmt.close();
                    conn.close();
                    //  bersih();
                } else {
                    JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
                }
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(FrameSalwa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        tampil();

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        String Id_Siswa = tfIdSiswa.getText();

        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);

            int jawab = JOptionPane.showConfirmDialog(null, "Silakan Konfirmasi?");
            switch (jawab) {
                case JOptionPane.YES_OPTION:
                    String deleteSql = "DELETE FROM Siswa WHERE Id_Siswa = ?";
                    pstmt = conn.prepareStatement(deleteSql);
                    pstmt.setString(1, Id_Siswa);
                    pstmt.executeUpdate();
                    break;

                case JOptionPane.NO_OPTION:
                    JOptionPane.showMessageDialog(this, "Kamu menjawab tidak");
                    break;
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Cek Lagi!!!", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } finally {
            if (pstmt != null) {
                try {
                    pstmt.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }
        tampil();

    }//GEN-LAST:event_btnDeleteActionPerformed

